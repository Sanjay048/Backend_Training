package Day1;

public class allPrimitiveType {
    public static void main(String[] args) {
        byte one = 10;
        short two = 20;
        int three = 30;
        long four = 40;

        boolean five = true;

        char six = 'S';

        float seven = 12.3f;
        double eight = 123.2345;

        System.out.println("byte :"+one+"\nshort :"+two+"\nint :"+three+"\nlong :"+four+"\nboolean :"+five+"\nchar :"+six+"\nfloat :"+seven+"\ndouble :"+eight);
        System.out.println("Convert a double to an int :"+(int)eight);

    }
}
